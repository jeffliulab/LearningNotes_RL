

def replace_grid_characters(grid_str):
    """
    将网格字符串中的字符按照以下规则替换：
    0 -> E
    1 -> 0
    2 -> S
    3 -> F
    """
    # 使用临时字符作为占位符
    temp_str = grid_str.replace('0', 'X')
    # 依次替换其他字符
    temp_str = temp_str.replace('1', '0')
    temp_str = temp_str.replace('2', 'S')
    temp_str = temp_str.replace('3', 'F')
    # 最后将临时字符替换为目标字符
    result_str = temp_str.replace('X', 'E')
    return result_str

